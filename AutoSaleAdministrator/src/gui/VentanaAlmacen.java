    private void realizarVenta() {
        Cliente c = null;
        Vehiculo v = null;
        Registro r = null;
        if (CjMatricula.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "SeÃ±or usuario, no se digito ninguna matricula");
            return ;
        }
        if (CjNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "SeÃ±or usuario, no se digito ningun nombre");
            return ;
        }
        if (CjCedula.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "SeÃ±or usuario, no se digito ninguna cedula");
            return ;
        }
        if (ConeccionBaseDatos.getAlmacen().buscarVehiculoMatricula(CjMatricula.getText())){
            c = new Cliente(Integer.parseInt(CjCedula.getText()), CjNombre.getText());
            v = ConeccionBaseDatos.getAlmacen().buscarVehiculo(CjMatricula.getText());
            r = new Registro(c, v); 
        }else{
            JOptionPane.showMessageDialog(this, "SeÃ±or usuario, este vehiculo no se encuentra registrado");
            return ;
        }
        boolean rta = ConeccionBaseDatos.getVenta().agregarVenta(r);
        if (rta == true){
            this.actualizar();
            ConeccionBaseDatos.AgregarCliente(c, v);
            JOptionPane.showMessageDialog(this, "La venta se agrego correctamente "+v.getMatricula());
            ConeccionBaseDatos.conectar();
            setVisible(false);
            VentanaPrincipal ventanaPrincipal = new VentanaPrincipal();
            ventanaPrincipal.setVisible(true);
            ventanaPrincipal.setLocation(getLocation());
        }else {
           JOptionPane.showMessageDialog(this, "No se logro realizar la venta :C "+v.getMatricula()); 
        }
    }

    private void actualizar() {
        CjCedula.setText("");
        CjNombre.setText("");
        CjMatricula.setText("");
    }
    
    
